#include "ft_fdf.h"

int				mouse_pres(int k, void *q)
{
	t_fdf		*mlw;

	mlw = (t_fdf*)q;
	if ((k == kVK_Shift || k == kVK_CapsLock))
		mlw->shift = k;
	return (0);
}
