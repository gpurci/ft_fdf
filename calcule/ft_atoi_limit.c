#include "ft_fdf.h"

int						ft_atoi_limit(char *c, void *nbr)
{
	int						*k;
	int						i;

	k = (int*)nbr;
	if ((i = ft_atoi(c)) > -1 && i < 1000)
		*k = i;
	return (*k);
}
